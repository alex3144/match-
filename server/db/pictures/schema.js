'use strict';

import mongoose from 'mongoose';

// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

var picture = mongoose.Schema({
  score: {
    type: Number,
    required: true
  },
  state: {
    type: String,
    required: true,
    default: 'equal'
  },
  url: {
    type: String,
    required: true
  },
  dates: {
    created_at: Date
  }
});



// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------

module.exports = picture;